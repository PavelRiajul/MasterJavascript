//switch statement


let day = 1;

switch(day){
    case 0:
    console.log('saterday')
    break;
    case 1:
    console.log('saterday')
    break;
    case 2:
    console.log('sunday')
    break;
    case 3:
    console.log('monday')
    break;
    case 4:
    console.log('tuesday')
    break;
    case 5:
    console.log('wednesday')
    break;
    case 6:
    console.log('thrusday')
    break;
    case 7:
    console.log('friday')
    break;
    default:
        console.log('Invalid day')

}

